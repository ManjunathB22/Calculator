public class arithematicLogic {

    //Method for Multiplication
    public int getMul(int num1, int num2){
        int mul = num1*num2;
        return mul;
    }

    //Method for Addition
    public int getSum(int num1,int num2) {
        int sum=num1+num2;
        return sum;
    }

    public int getSub(int num1, int num2) {
        int sub = num1-num2;
        return sub;
    }
}
